#include<stdio.h>

int main (){
	int x=5, y = 5;
	printf("inital values: x = %d, y = %d \n", x,y);
//	Equal to
    printf("x == y: %d\n", x==y);
//    Greater than
  printf("x > y: %d\n", x>y);
//  Less than
   printf("x < y: %d\n", x<y);
//   Not equal to
   printf("x != y: %d\n", x!=y);
//   Greater than or equal to 
  printf("x >= y: %d\n", x>=y);
//  Less than or equal to
  printf("x <= y: %d\n", x<=y);
  return 0;
}
